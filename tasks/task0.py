# произведение чисел в диапазоне, начальное и конечное значение, которого вводится с клавиатуры:
num8 = int(input("Введите первое число диапазона: "))
num9 = int(input("Введите второе число диапазона: "))
mult = 1
step = 1
while num8 <= num9:
    num8 = num8 + step
    mult = mult * num8
print(str("Результат произведения: ") + str(mult))