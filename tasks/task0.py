# сумма всех чисел в диапазоне, начальное и конечное значение, которого вводится с клавиатуры:
num6 = int(input("Введите первое число диапазона: "))
num7 = int(input("Введите второе число диапазона: "))
sum3 = 0
while num6 <= num7:
    num6 = num6 + 1
    sum3 = sum3 + num6
